if __name__ == '__main__':
    tag = '0123456789'
    print(tag[0:]) # 0123456789
    print(tag[:-1]) # 012345678
    print(tag[::]) # 0123456789
    print(tag[3:5]) # 34 索引是从0开始的
    print(tag[-3:-1]) #78 最后一位 -1
    # 如果第一索引指定元素位于第二元素指定的元素后面，结果就是空序列

    print(tag[::2]) # 023468 2是步长
    print(tag[8:5:-1]) # 向后移动